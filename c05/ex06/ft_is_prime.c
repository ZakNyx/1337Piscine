int	ft_is_prime(int nb)
{
	int	pr;

	pr = 2;
	if (nb <= 1)
		return (0);
	while (nb % pr != 0)
	{
		pr++;
	}
	if (pr == nb)
	{
		return (1);
	}
	else
		return (0);
}
